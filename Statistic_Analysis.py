import scipy.stats as stats
import numpy as np

def compare_distributions(accuracyA, accuracyB, timeA, timeB):
    if accuracyA != None and accuracyB != None:
        # Convert to numpy arrays so we can use .mean()
        accuracyA = np.array(accuracyA)
        accuracyB = np.array(accuracyB)
        _, accuracy_p = stats.wilcoxon(accuracyA, accuracyB, correction=False)
        print("Significance of Accuracy: {}".format(accuracy_p))
        print("Accuracy A - Mean = {:.16f}, STD = {:.16f}".format(accuracyA.mean(), accuracyA.std()))
        print("Accuracy B - Mean = {:.16f}, STD = {:.16f}".format(accuracyB.mean(), accuracyB.std()))

    if timeA != None and timeB != None:
        timeA = np.array(timeA)
        timeB = np.array(timeB)
        _, time_p = stats.wilcoxon(timeA, timeB, correction=False)
        print("Significance of Computation Time: {}".format(time_p))
        print("Time A - Mean = {:.9f}, STD = {:.9f}".format(timeA.mean(), timeA.std()))
        print("Time B - Mean = {:.9f}, STD = {:.9f}".format(timeB.mean(), timeB.std()))
        print()

